// Primitive
const name = 'Harvey Norman'; // a string
console.log(name);

const age = 20; // a number
console.log(age);

const isStudent = true; // a boolean
console.log(isStudent);

// Advanced
const colors = ['Red', 'Blue', 'Green']; // an array
console.log(colors);

const person = {
	name: 'Harvey Norman',
	age: 20,
	isStudent: true,
}; // an object
console.log(person);

function printMessage(message) {
	console.log(message);
} //function
printMessage('howdy!');

// Special
let occupation; // undefined
console.log(occupation);

const currentCart = null; // null
console.log(currentCart);
